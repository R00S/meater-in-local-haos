#
# Staples (oil, butter, salt, pepper, sugar, vinegar) are assumed available
# and NOT listed — freeing slots for more distinctive ingredients & proteins.
# ---------------------------------------------------------------------------

def _ing(id_str, name):
    """Common ingredient — shown by default in this cuisine's ingredient panel."""
    return {"id": id_str, "name": name, "common": True}

def _inge(id_str, name):
    """Extended ingredient — hidden behind 'More' in this cuisine's ingredient panel.
    The same ingredient can be _ing() in one cuisine and _inge() in another."""
    return {"id": id_str, "name": name, "common": False}

